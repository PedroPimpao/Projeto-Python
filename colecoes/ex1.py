# Lista reconhece todos os tipos de dados. Ordenada e mutavel. Permite membros duplicados 
lista = ['carro', True, 2, 2.3]

# for elemento in lista:
#     print(elemento)

print(lista)
print(type(lista))
print('-'*50)

# Tupla
# Ordenada e imutavel. Permite membros duplicados
tupla = ('carro', True, 2, 2.3)
print(tupla)
print(type(tupla))
print('-'*50)

# Dicionario
# O dicionario é uma colecao ordenada e mutavel. Nenhum membro duplicado
dicionario = {"chave": "valor"}
print(dicionario)
print(type(dicionario))
print('-'*50)

# Conjuntos
# Set é uma coleção não ordenada e não indexada. Nenhum membro duplicado 
conjunto = {"carro", True, 2, 2.35}
print(conjunto)
print(type(conjunto))